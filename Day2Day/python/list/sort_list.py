my_list = [34,0,23,33,4,2,3]

print("before sorting...");
print(my_list)
print("after sorting...using sort() method..")
my_list.sort()
print(my_list)

print("\n")
print("sorting in decending order..")
my_list.sort(reverse = True)
print(my_list)


